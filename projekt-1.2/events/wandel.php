Christian Wandel er en rutineret konservatorieuddannet sanger og pianist, som har optrådt i mange sammenhænge rundt omkring i Danmark. I 2012 udgav han debut-CD’en ”Indtil vi ses” og siden hen to singler.

Han er gennem tiden blevet inspireret af kunstnere som: Mads Langer, Michael Bublé, Stevie Wonder, Elton John, Tim Christensen m.fl.

Repertoiret spænder over en bred kam af genrer og kunstnere inden for bl.a. pop, jazz, soul og evergreens fra både ind- og udland.

"Jeg giver musikken lov til at være musik i nuet og lader de impulser jeg modtager fra omgivelserne forme den. På den måde undgår musikken at blive ensformig og kedelig, idet den får et nyt liv hver gang jeg spiller den – og dermed bliver den også mere spændende at lytte til og mere nærværende."

